package Model.Domain;

public class Paypal
{
    String email;
    String codice;

    public void setEmail(String email) {
        this.email = email;
    }

    public void setCodice(String codice) {
        this.codice = codice;
    }

    public String getEmail() {
        return email;
    }

    public String getCodice() {
        return codice;
    }
}
